package com.callor.hello.algebra;

public class BooleanA {
	
	public static void main(String[] args) {
		System.out.println(30 > 40);
		System.out.println(30 < 40);
		System.out.println(30 > 30);
		System.out.println(30 >= 30);
		System.out.println(30 <= 30);
		// 코딩에서는 =는 ==개로 해야 인식 
		// 아래 노란색 밑줄은 당연한 값을 왜 묻냐는거
		System.out.println(30 == 30);
		
	}

}

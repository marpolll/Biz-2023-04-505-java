package com.marpol.utils;

public class Utils {
	public static final String dLine = "=".repeat(60);
	public static final String sLine = "-".repeat(60);
	
	public static String dLine(int lenght) {
		return "=".repeat(lenght);
	}
	public static String sLine(int lenght) {
		return "-".repeat(lenght);
	}

}

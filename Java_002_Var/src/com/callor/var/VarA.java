package com.callor.var;

public class VarA {
	
	public static void main(String[] args) {
		
		int Num1 = 0;
		int Num2 = 0;
		
		Num1 = 30;
		Num2 = 40;
		
		System.out.println(Num1 + Num2);
		System.out.println(Num1 - Num2);
		System.out.println(Num1 * Num2);
		System.out.println(Num1 / Num2);
		System.out.println(Num1 % Num2);
		
	}

}

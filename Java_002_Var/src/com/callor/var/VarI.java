package com.callor.var;

public class VarI {

	public static void main(String[] args) {

		int num = 0;
		// num = num + 1; = num += 1; = num ++; num변수에 저장된 값을 1증가 할때만
		num = num + 1;
		num += 1;
		num++;
		// 아래식도 num 변수에 저장된 값을 1감소할때는 num --; 사용 가능
		num = num - 1;
		num -= 1;
		num--;
		
		num++;
		num++;
		num++;
		num++;
		num++;
		
	}
}

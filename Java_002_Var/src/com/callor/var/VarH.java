package com.callor.var;

public class VarH {
	public static void main(String[] args) {
		int num1 = 0;

		// 간소화 해서 적용가능
		//num1 = num1 + 10;  =  num1 += 10;

		num1 = num1 + 10;
		num1 += 10;

		num1 = num1 - 5;
		num1 -= 5;

		num1 = num1 * 3;
		num1 *= 5;

		num1 = num1 / 2;
		num1 /= 2;

		num1 = num1 % 2;
		num1 %= 2;
	}

}

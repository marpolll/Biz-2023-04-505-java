package com.callor.var;

public class VarG {
	
	public static void main(String[] args) {
		
		int num1 = 30;
		int num2 = 43;
		
		// 데이터 type 이 true, false 로만 나타나는 boolean
		boolean bEven1 = num1 % 2 == 0 ;
		boolean bEven2 = num2 % 2 == 0 ;
		
		System.out.println(bEven1);
		System.out.println(bEven2);
		
	}

}
